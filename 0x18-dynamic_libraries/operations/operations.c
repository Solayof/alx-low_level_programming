#include "operations.h"

/**
 * add - add two operand
 * @a: first operand
 * @b: second operand
 * Return: return the sum.
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two operands
 * @a: first operand
 * @b: second operand
 * Return: the subtract.
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
* mul - mutiplication two operands
* @a: first operand
* @b: second operand
* Return: the multiplication.
*/

int mul(int a, int b)
{
	return (a * b);
}

/**
* div - divides two operands
* @a: first operand
* @b: second operand
* Return: the division.
*/
int div(int a, int b)
{
	return (a / b);
}

/**
* mod - modular division two operands
* @a: first operand
* @b: second operand
* Return: the modular.
*/
int mod(int a, int b)
{
	return (a % b);
}
